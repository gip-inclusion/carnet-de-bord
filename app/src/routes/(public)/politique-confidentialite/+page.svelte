		<h2>Finalité</h2>
		<p>Carnet de bord en tant que service numérique du suivi de parcours a pour finalités :</p>
		<ol>
			<li>
				La mise en œuvre d’un service permettant de recenser les informations utiles et à jour
				relatives à la situation des personnes et aux actions engagées ou à engager pendant le
				parcours d’insertion et de la partager avec les acteurs qui agissent sur le parcours dans
				leur domaine d’intervention ;
			</li>
			<li>
				L'échange ascendant et descendant de données entre le carnet de bord et les acteurs de
				l’insertion ;
			</li>
			<li>
				L’accès direct à ces informations et leur actualisation par tous avec les acteurs de
				l’insertion qui interviennent, au titre de l’accompagnement social et/ou professionnel, dans
				le parcours en co- construction avec la personne ;
			</li>
			<li>
				La restitution sous format papier à la personne des informations relatives à son parcours
				pour favoriser sa mobilisation
			</li>
		</ol>

