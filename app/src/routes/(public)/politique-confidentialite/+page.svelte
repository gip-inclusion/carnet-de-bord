	<h2 id="cookies">Cookies</h2>
	<p>
		Un cookie est un fichier déposé sur votre terminal lors de la visite d’un site. Il a pour but de
		collecter des informations relatives à votre navigation et de vous adresser des services adaptés
		à votre terminal (ordinateur, mobile ou tablette).
	</p>
	<p>
		En application de l’article 5(3) de la directive 2002/58/CE modifiée concernant le traitement
		des données à caractère personnel et la protection de la vie privée dans le secteur des
		communications électroniques, transposée à l’article 82 de la loi n°78-17 du 6 janvier 1978
		relative à l’informatique, aux fichiers et aux libertés, les traceurs ou cookies suivent deux
		régimes distincts.
	</p>
	<p>
		Les cookies strictement nécessaires au service ou ayant pour finalité exclusive de faciliter la
		communication par voie électronique sont dispensés de consentement préalable au titre de
		l’article 82 de la loi n°78-17 du 6 janvier 1978. Les cookies n’étant pas strictement
		nécessaires au service ou n’ayant pas pour finalité exclusive de faciliter la communication par
		voie électronique doivent être consenti par l’utilisateur.
	</p>
	<p>
		Ce consentement de la personne concernée pour une ou plusieurs finalités spécifiques constitue
		une base légale au sens du RGPD et doit être entendu au sens de l’article 6-a du Règlement (UE)
		2016/679 du Parlement européen et du Conseil du 27 avril 2016 relatif à la protection des
		personnes physiques à l’égard du traitement des données à caractère personnel et à la libre
		circulation de ces données.
	</p>
	<p>
		Le site dépose des cookies de mesure d’audience (nombre de visites, pages consultées),
		respectant les conditions d’exemption du consentement de l’internaute définies par la
		recommandation « Cookies » de la Commission nationale informatique et libertés (CNIL). Cela
		signifie, notamment, que ces cookies ne servent qu’à la production de statistiques anonymes et
		ne permettent pas de suivre la navigation de l’internaute sur d’autres sites.
	</p>
	<p>
		Nous utilisons pour cela Matomo, un outil de mesure d’audience web libre, paramétré pour être en
		conformité avec la recommandation « Cookies » de la CNIL. Nous n’utilisons pas Matomo en mode
