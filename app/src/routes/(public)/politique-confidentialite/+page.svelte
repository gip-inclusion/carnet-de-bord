		<h2>Base juridique du traitement de données</h2>
		<p>
			Les données traitées à l’occasion de ces traitements ont plusieurs fondements juridiques :
		</p>
		<ul>
			<li>
				L’exécution d’une mission d’intérêt public ou relevant de l’exercice de l’autorité publique
				dont est investi le responsable de traitement au sens de l’article 6-e du RPGD ;
			</li>
			<li>
				L’obligation légale à laquelle est soumise le responsable de traitements au sens de
				l’article 6-c du RGPD ;
			</li>
		</ul>
		<h3>Ces fondements sont précisés ci-dessous :</h3>
		<h4>
			a. Données d’identité, de situation au regard de l'aide sociale, de conditions de vie
			matérielles, données sensibles
		</h4>
		<p>
			Ce traitement de données est nécessaire à l’exercice d’une mission d’intérêt public ou
			relevant de l’exercice de l’autorité publique dont est investi le responsable de traitement au
			sens de l’article 6-e du règlement (UE) 2016/679 du Parlement européen et du Conseil du 27
			avril 2016 relatif à la protection des personnes physiques à l’égard du traitement des données
			à caractère personnel et à la libre circulation de ces données.
		</p>
		<h4>b. Données de connexion</h4>
		<p>
			Ce traitement est nécessaire au respect d'une obligation légale à laquelle le responsable de
			traitement est soumis au sens de l'article 6-c du Règlement (UE) 2016/679 du Parlement
			européen et du Conseil du 27 avril 2016 relatif à la protection des personnes physiques à
			l'égard du traitement des données à caractère personnel et à la libre circulation de ces
			données.
		</p>
		<p>
			L'obligation légale est posée par la loi LCEN n° 2004-575 du 21 juin 2004 pour la confiance
			dans l'économie numérique et par l’article 1 du décret n° 2021-1363 du 20 octobre 2021.
		</p>
		<h4>c. Cookies</h4>
		<p>
			En application de l’article 5(3) de la directive 2002/58/CE modifiée concernant le traitement
			des données à caractère personnel et la protection de la vie privée dans le secteur des
			communications électroniques, transposée à l’article 82 de la loi n°78-17 du 6 janvier 1978
			relative à l’informatique, aux fichiers et aux libertés, les traceurs ou cookies suivent deux
			régimes distincts.
		</p>
		<p>
			Les cookies strictement nécessaires au service ou ayant pour finalité exclusive de faciliter
			la communication par voie électronique sont dispensés de consentement préalable au titre de
			l’article 82 de la loi n°78-17 du 6 janvier 1978.
		</p>
		<p>
			Les cookies n’étant pas strictement nécessaires au service ou n’ayant pas pour finalité
			exclusive de faciliter la communication par voie électronique doivent être consenti par
			l’utilisateur.
		</p>
		<p>
			Ce consentement de la personne concernée pour une ou plusieurs finalités spécifiques constitue
			une base légale au sens du RGPD et doit être entendu au sens de l'article 6-a du Règlement
			(UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016 relatif à la protection des
			personnes physiques à l'égard du traitement des données à caractère personnel et à la libre
			circulation de ces données.
		</p>
		<p>
			En l’occurrence Carnet de bord n’utilise pas de cookies nécessitant le consentement. En effet,
			l’outil utilisé est Matomo, un outil libre, paramétré pour être en conformité avec la
