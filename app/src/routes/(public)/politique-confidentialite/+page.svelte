		<h2>Utilisation de témoins de connexion (« cookies »)</h2>
		<p>
			Un cookie est un fichier déposé sur votre terminal lors de la visite d’un site. Il a pour but
			de collecter des informations relatives à votre navigation et de vous adresser des services
			adaptés à votre terminal (ordinateur, mobile ou tablette).
		</p>
		<p>
			Nous collectons donc des données par l’intermédiaire de dispositifs appelés “cookies”
			permettant d’établir des mesures statistiques. Le site dépose des cookies de mesure d’audience
			(nombre de visites, pages consultées), respectant les conditions d’exemption du consentement
			de l’internaute définies par la recommandation « Cookies » de la Commission nationale
			informatique et libertés (CNIL). Cela signifie, notamment, que ces cookies ne servent qu’à la
			production de statistiques anonymes et ne permettent pas de suivre la navigation de
			l’internaute sur d’autres sites.
		</p>
		<p>
			Le site dépose également des cookies de navigation, aux fins strictement techniques, qui ne
			sont pas conservés. La consultation du site n’est pas affectée lorsque les utilisateurs
			utilisent des navigateurs désactivant les cookies.
		</p>
		<p>Nous utilisons différents types de cookies, certains nécessitant votre consentement.</p>

