	<h2 id="quels-sont-vos-droits-">Quels sont vos droits ?</h2>
	<p>
		Conformément aux dispositions du RGPD, vous disposez des droits suivants sur vos données à
		caractère personnel :
	</p>
	<ul>
		<li><strong>Droit à l’information et droit d’accès ;</strong></li>
		<li><strong>Droit à la rectification</strong> ;</li>
		<li><strong>Droit à la limitation des données ;</strong></li>
		<li><strong>Droit d’opposition</strong>.</li>
	</ul>
	<p>
		En raison de l’obligation de sécurité et de confidentialité dans le traitement des données à
		caractère personnel qui incombe au responsable de traitement, les demandes des personnes
		concernées ne seront traitées que si nous sommes en mesure de vous identifier de façon certaine.
	</p>
	<p>
		En cas de doute sérieux sur votre identité, nous pouvons être amenés à vous demander la
		communication d’une preuve d’identité.
	</p>
	<p>
		Pour vous aider dans votre démarche, vous trouverez un modèle de courrier élaboré par la CNIL
		ici :
		<a href="https://www.cnil.fr/fr/modele/courrier/exercer-son-droit-dacces">
			https://www.cnil.fr/fr/modele/courrier/exercer-son-droit-dacces
